#include<iostream>

using namespace std;

int main() {
	int num;
	cout << "Please enter some number " << endl;
	cin >> num;
	
	if (num % 2 == 0)
	{
		cout << "The number you have entered is even number" << endl;
		
	}
	else
	{
		cout << "The number you have entered is odd number" << endl;
	}
	
		
	
	return 0;
}
